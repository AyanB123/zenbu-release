import fs from "node:fs"
import fsp from "node:fs/promises"
import path from "node:path"

/**
 * Write the given plugin manifest path into the user's local JSONC
 * plugins overlay.
 *
 * As of the JSONC migration the overlay is `zenbu.plugins.local.jsonc`
 * (gitignored). This function is the single point of truth used by both
 * `plugin-installer` (clone-and-install) and `plugin-dev` (Install
 * Plugin button on a locally scaffolded plugin) so the on-disk state is
 * consistent across entry points.
 *
 * Strategy:
 *
 *   1. If the file doesn't exist, create it with `{ "plugins": [...] }`
 *      and a header comment explaining how to edit it.
 *   2. If the file exists, parse it (JSONC \u2014 comments + trailing
 *      commas tolerated), check whether the manifest path is already
 *      present, and append if not. If a previously-disabled entry
 *      exists, flip it back to `enabled: true` instead of duplicating.
 *
 * The function is text-aware but not AST-aware: when we rewrite the file
 * we emit a canonical layout (two-space indent, one entry per line) and
 * drop any prior comments. This is the same trade-off the original
 * `zenbu.local.ts` patcher made; the local overlay is shape-restricted
 * and machine-edited, so losing comments on rewrite is acceptable.
 */
export async function patchLocalPlugins(
  projectDir: string,
  manifestPath: string,
): Promise<void> {
  const overlayPath = path.join(projectDir, "zenbu.plugins.local.jsonc")
  const absManifest = path.isAbsolute(manifestPath)
    ? manifestPath
    : path.resolve(projectDir, manifestPath)

  if (!fs.existsSync(overlayPath)) {
    const body = renderOverlay(
      [{ path: absManifest, enabled: true }],
      { withHeader: true },
    )
    await fsp.writeFile(overlayPath, body, "utf8")
    return
  }

  const raw = await fsp.readFile(overlayPath, "utf8")
  let parsed: unknown
  try {
    parsed = parseJsonc(raw)
  } catch {
    // Corrupt/unparseable overlay \u2014 keep the bad file around as a
    // sibling so the user can recover, then rewrite.
    const backupPath = nextAvailablePath(
      projectDir,
      "zenbu.plugins.local.previous",
      ".jsonc",
    )
    await fsp.rename(overlayPath, backupPath)
    const body = renderOverlay(
      [{ path: absManifest, enabled: true }],
      { withHeader: true },
    )
    await fsp.writeFile(overlayPath, body, "utf8")
    return
  }

  const entries: Array<{ path: string; enabled?: boolean; args?: unknown }> =
    isPlainObject(parsed) && Array.isArray((parsed as { plugins?: unknown }).plugins)
      ? ((parsed as { plugins: unknown[] }).plugins.filter(isPlainObject) as Array<{
          path: string
          enabled?: boolean
          args?: unknown
        }>)
      : []

  const overlayDir = path.dirname(overlayPath)
  const normalize = (entryPath: string): string =>
    path.isAbsolute(entryPath) ? entryPath : path.resolve(overlayDir, entryPath)

  const existing = entries.find((e) => normalize(e.path) === absManifest)
  if (existing) {
    if (existing.enabled !== false) return // already enabled, idempotent
    existing.enabled = true
  } else {
    entries.push({ path: absManifest, enabled: true })
  }

  await fsp.writeFile(overlayPath, renderOverlay(entries, { withHeader: false }), "utf8")
}

/**
 * Walk up from `startDir` (defaults to `process.cwd()`) looking for the
 * project root — the directory that contains `zenbu.config.ts`. We
 * patch `zenbu.plugins.local.jsonc` next to it.
 */
export function findProjectRoot(startDir?: string): string {
  let dir = startDir ?? process.cwd()
  for (let i = 0; i < 12; i++) {
    if (fs.existsSync(path.join(dir, "zenbu.config.ts"))) return dir
    const parent = path.dirname(dir)
    if (parent === dir) break
    dir = parent
  }
  return startDir ?? process.cwd()
}

