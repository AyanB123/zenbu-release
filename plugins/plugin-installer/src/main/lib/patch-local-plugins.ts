//                                  internals

function renderOverlay(
  entries: Array<{ path: string; enabled?: boolean; args?: unknown }>,
  opts: { withHeader: boolean },
): string {
  const lines: string[] = []
  if (opts.withHeader) {
    lines.push(
      "// Auto-generated by the plugin-installer plugin.",
      "// Per-developer overrides \u2014 entries here are layered on top of",
      "// `zenbu.plugins.jsonc`. Flip `enabled` to disable a plugin without",
      "// removing it. Edits hot-reload like any other JSONC manifest.",
    )
  }
  lines.push("{")
  lines.push(`  "plugins": [`)
  entries.forEach((entry, idx) => {
    const parts: string[] = []
    parts.push(`"path": ${JSON.stringify(entry.path)}`)
    if (entry.enabled !== undefined) {
      parts.push(`"enabled": ${entry.enabled ? "true" : "false"}`)
    }
    if (entry.args !== undefined) {
      parts.push(`"args": ${JSON.stringify(entry.args)}`)
    }
    const comma = idx < entries.length - 1 ? "," : ""
    lines.push(`    { ${parts.join(", ")} }${comma}`)
  })
  lines.push(`  ]`)
  lines.push("}")
  return lines.join("\n") + "\n"
}

/**
 * Minimal JSONC parser \u2014 strips line/block comments and trailing
 * commas, then runs `JSON.parse`. Same shape as the helper inside
 * `@zenbujs/core`'s plugin-manifests module; duplicated here so the
 * plugin doesn't have to reach into core internals at runtime.
 */
function parseJsonc(source: string): unknown {
  let out = ""
  let i = 0
  const n = source.length
  while (i < n) {
    const ch = source[i]
    if (ch === '"') {
      const start = i
      i++
      while (i < n) {
        const c = source[i]
        if (c === "\\") {
          i += 2
          continue
        }
        if (c === '"') {
          i++
          break
        }
        i++
      }
      out += source.slice(start, i)
      continue
    }
    if (ch === "/" && source[i + 1] === "/") {
      i += 2
      while (i < n && source[i] !== "\n") i++
      continue
    }
    if (ch === "/" && source[i + 1] === "*") {
      i += 2
      while (i < n && !(source[i] === "*" && source[i + 1] === "/")) i++
      i = Math.min(i + 2, n)
      continue
    }
    out += ch
    i++
  }
  const cleaned = out.replace(/,\s*(?=[\]}])/g, "")
  return JSON.parse(cleaned)
}

function isPlainObject(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value)
}

function nextAvailablePath(
  dir: string,
  base: string,
  ext: string,
): string {
  let candidate = path.join(dir, `${base}${ext}`)
  let n = 1
  while (fs.existsSync(candidate)) {
    n++
    candidate = path.join(dir, `${base}.${n}${ext}`)
  }
  return candidate
}
